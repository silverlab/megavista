 function [D11,D12,D21,D22] = setstars(mu,lambda)
  a        = 2*mu+lambda;
  b        = mu;
  c        = (mu+lambda)/4;
  D11 = [0 a 0; b -2*(a+b) b;0 a 0];
  D12 = [c 0 -c;0 0 0;-c 0 c];
  D21 = [c 0 -c;0 0 0;-c 0 c];
  D22 = [0 b 0; a -2*(a+b) a;0 b 0];
  %D11 = [0 1 0;1 -4 1;0 1 0];
  %D12 = zeros(3);
  %D21 = zeros(3);
  %D22 = [0 1 0;1 -4 1;0 1 0];
return;
